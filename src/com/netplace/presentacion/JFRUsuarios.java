    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            int id = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            int opcion = JOptionPane.showConfirmDialog(this, "Esta seguro que desea eliminar", "Pregunta", JOptionPane.YES_NO_OPTION);
            if (opcion == JOptionPane.YES_OPTION) {
                userLN.deleteUser(id);
                getUser(userLN.getUsers());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se puede realizar esta accion porque debe seleccionar un registro", "Seleccione un registro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            User user = userLN.getUserId(Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString()));
            JFREditarUsuario editUser = new JFREditarUsuario();
            OpenInternalFrame.charge(editUser, desktop);

            editUser.txtId.setText("" + user.getIdUser());
            editUser.txtName.setText(user.getName());
            editUser.txtUserName.setText(user.getUsername());
            editUser.jPasswordField1.setText(user.getPassword());
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se puede realizar esta accion porque debe seleccionar un registro", "Seleccione un registro", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jButton3ActionPerformed

