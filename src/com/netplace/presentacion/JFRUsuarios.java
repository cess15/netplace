    public void getUser(List<User> lUser) {
        data = new Object[3];
        model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int i, int i1) {
                return false;
            }

        };
        jTable1.setModel(model);
        model.addColumn("ID");
        model.addColumn("Nombre");
        model.addColumn("Nombre de Usuario");
        jTable1.removeAll();

        for (User user : lUser) {
            data[0] = user.getIdUser();
            data[1] = user.getName();
            data[2] = user.getUsername();
            model.addRow(data);
        }
    }

    public void search() {
        getUser(userLN.getUsers());
        if (!this.jTextField1.getText().isEmpty()) {
            List<User> lUser = new ArrayList<User>();
            List<User> lUsers = userLN.getUsers();
            for (User user : lUsers) {
                if (user.getName().toLowerCase().contains(this.jTextField1.getText().toLowerCase())
                        || (user.getUsername().toLowerCase().contains(this.jTextField1.getText().toLowerCase()))) {
                    User u = new User();
                    u.setIdUser(user.getIdUser());
                    u.setName(user.getName());
                    u.setUsername(user.getUsername());
                    lUser.add(u);
                }
            }
            getUser(lUser);
        }
    }

