    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            Product product = productLN.getProductId(Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString()));
            JFREditarProducto editProduct = new JFREditarProducto();
            OpenInternalFrame.charge(editProduct, desktop);

            editProduct.txtId.setText("" + product.getIdProduct());
            editProduct.txtName.setText(product.getName());
            editProduct.txtPrice.setText("" + product.getSalePrice());
            editProduct.txtDescription.setText(product.getDescription());
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se puede realizar esta accion porque debe seleccionar un registro", "Seleccione un registro", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            int id = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            int opcion = JOptionPane.showConfirmDialog(this, "Esta seguro que desea eliminar", "Pregunta", JOptionPane.YES_NO_OPTION);
            if (opcion == JOptionPane.YES_OPTION) {
                productLN.deleteProduct(id);
                getProduct(productLN.getProducts());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se puede realizar esta accion porque debe seleccionar un registro", "Seleccione un registro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

