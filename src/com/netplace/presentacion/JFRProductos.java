    public void getProduct(List<Product> lProduct) {
        data = new Object[5];
        model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int i, int i1) {
                return false;
            }

        };
        jTable1.setModel(model);
        model.addColumn("ID");
        model.addColumn("Nombre");
        model.addColumn("Precio");
        model.addColumn("Stock");
        model.addColumn("Descripcion");
        jTable1.removeAll();

        for (Product product : lProduct) {
            data[0] = product.getIdProduct();
            data[1] = product.getName();
            data[2] = product.getSalePrice();
            data[3] = product.getStock();
            data[4] = product.getDescription();
            model.addRow(data);
        }
    }

    public void search() {
        getProduct(productLN.getProducts());
        if (!this.jTextField1.getText().isEmpty()) {
            List<Product> lProduct = new ArrayList<Product>();
            List<Product> products = productLN.getProducts();
            for (Product product : products) {
                if (product.getName().toLowerCase().contains(this.jTextField1.getText().toLowerCase())
                        || (product.getDescription().toLowerCase().contains(this.jTextField1.getText().toLowerCase()))) {
                    Product p = new Product();
                    p.setIdProduct(product.getIdProduct());
                    p.setName(product.getName());
                    p.setSalePrice(product.getSalePrice());
                    p.setStock(product.getStock());
                    p.setDescription(product.getDescription());
                    lProduct.add(p);
                }
            }
            getProduct(lProduct);
        }
    }

